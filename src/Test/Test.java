    private void btngetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngetActionPerformed
        txtdisplay.setText("");
        String times[]= getTimes(movietimes);
        if (times[0].equals("")){
            txtdisplay.append("\nERROR: " + txtmovie.getText() + " is an Invalid movie name\n");
            txtmovie.setText("");
            txtmovie.requestFocus();
        }
        else{
            
            txtdisplay.append("\nHere are the movie times for " + txtmovie.getText() + "\n" );
            txtmovie.setText("");
            txtmovie.requestFocus();
            for (int i = 0; i < times.length; i++) {
                txtdisplay.append(times[i] + "\n");
            }
        }
            

    }//GEN-LAST:event_btngetActionPerformed

