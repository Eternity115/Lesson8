    public String[] getTimes(String[][] t ){
        String times="";
        String movie = txtmovie.getText();
        for (int i = 0; i < t.length; i++) {
            if (movie.equals(t[i][1])){
                times += t[i][0] + ",";
            }
        }
        return times.split(",");
    }

